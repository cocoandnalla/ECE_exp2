module bin2bcd(clk, rst, bin, bcd);

input clk, rst;
input [7:0] bin;
output reg [7:0] bcd;

always @(negedge rst or posedge clk) begin
    if(!rst) begin
    bcd <= {4'b0000, 4'b0000};
    end
    else begin
        case(bin)
        0  : bcd <= {4'b0000, 4'b0000};
        1  : bcd <= {4'b0000, 4'b0001};
        2  : bcd <= {4'b0000, 4'b0010};
        3  : bcd <= {4'b0000, 4'b0011};
        4  : bcd <= {4'b0000, 4'b0100};
        5  : bcd <= {4'b0000, 4'b0101};
        6  : bcd <= {4'b0000, 4'b0110};
        7  : bcd <= {4'b0000, 4'b0111};
        8  : bcd <= {4'b0000, 4'b1000};
        9  : bcd <= {4'b0000, 4'b1001};
        10 : bcd <= {4'b0001, 4'b0000};
        11 : bcd <= {4'b0001, 4'b0001};
        12 : bcd <= {4'b0001, 4'b0010};
        13 : bcd <= {4'b0001, 4'b0011};
        14 : bcd <= {4'b0001, 4'b0100};
        15 : bcd <= {4'b0001, 4'b0101};
        16 : bcd <= {4'b0001, 4'b0110};
        17 : bcd <= {4'b0001, 4'b0111};
        18 : bcd <= {4'b0001, 4'b1000};
        19 : bcd <= {4'b0001, 4'b1001};
        20 : bcd <= {4'b0010, 4'b0000};
        21 : bcd <= {4'b0010, 4'b0001};
        22 : bcd <= {4'b0010, 4'b0010};
        23 : bcd <= {4'b0010, 4'b0011};
        24 : bcd <= {4'b0010, 4'b0100};
        25 : bcd <= {4'b0010, 4'b0101};
        26 : bcd <= {4'b0010, 4'b0110};
        27 : bcd <= {4'b0010, 4'b0111};
        28 : bcd <= {4'b0010, 4'b1000};
        29 : bcd <= {4'b0010, 4'b1001};
        30 : bcd <= {4'b0011, 4'b0000};
        31 : bcd <= {4'b0011, 4'b0001};
        32 : bcd <= {4'b0011, 4'b0010};
        33 : bcd <= {4'b0011, 4'b0011};
        34 : bcd <= {4'b0011, 4'b0100};
        35 : bcd <= {4'b0011, 4'b0101};
        36 : bcd <= {4'b0011, 4'b0110};
        37 : bcd <= {4'b0011, 4'b0111};
        38 : bcd <= {4'b0011, 4'b1000};
        39 : bcd <= {4'b0011, 4'b1001};
        40 : bcd <= {4'b0100, 4'b0000};
        41 : bcd <= {4'b0100, 4'b0001};
        42 : bcd <= {4'b0100, 4'b0010};
        43 : bcd <= {4'b0100, 4'b0011};
        44 : bcd <= {4'b0100, 4'b0100};
        45 : bcd <= {4'b0100, 4'b0101};
        46 : bcd <= {4'b0100, 4'b0110};
        47 : bcd <= {4'b0100, 4'b0111};
        48 : bcd <= {4'b0100, 4'b1000};
        49 : bcd <= {4'b0100, 4'b1001};
        50 : bcd <= {4'b0101, 4'b0000};
        51 : bcd <= {4'b0101, 4'b0001};
        52 : bcd <= {4'b0101, 4'b0010};
        53 : bcd <= {4'b0101, 4'b0011};
        54 : bcd <= {4'b0101, 4'b0100};
        55 : bcd <= {4'b0101, 4'b0101};
        56 : bcd <= {4'b0101, 4'b0110};
        57 : bcd <= {4'b0101, 4'b0111};
        58 : bcd <= {4'b0101, 4'b1000};
        59 : bcd <= {4'b0101, 4'b1001};
        default bcd <= {4'b0000, 4'b0000};
        endcase
    end 
    end      
endmodule
